	--Создать таблицу
--New Table это идентификатор с ограничителями, 
--и поэтому он заключен в квадратные скобки.
create table [New_Table_374_3] (ColumnA int, ColumnB char(30))
go
--Чтобы заблокировать вывод сообщений с числом строк
