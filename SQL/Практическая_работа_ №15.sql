--set nocount off
go


	--Объявить необходимую пременную
declare @MyCounter int
--Инициализировать пременную
set @MyCounter = 0

--Определение число циклов с помощью переменной
while (@MyCounter < 26)
begin
--Вставлять в таблицу строку
insert into [New_Table_374_3] values
--С помощью переменной получить целочисленное значение для столбца
--ColumnA и сгенерировать уникальную букву для каждой строки.
-- Получить целочисленное значение символа “a” с помощью функции ASSCII
--Прибавить @MyCounter. С помощью функции CHAR преобразовать сумму 
--обратно в символы @MyCounter, следующие после символа  “a”.
(@MyCounter + 1, char ((@MyCounter + ascii('a'))))
--Увеличить значение переменной для учета этой итерации цикла
set @MyCounter = @MyCounter + 1
end
go

set nocount off
go

--должно вывестись 26 строк, а значения в двух столбцах 
--постепенно увеличиваются.
